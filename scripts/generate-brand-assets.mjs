const run = async () => {
  await ensureDir(brandDir);

  const logoMarkSvg = await readFile(resolve(assetsDir, "logo-mark.svg"));

  await renderPng(logoMarkSvg, resolve(brandDir, "icon-16.png"), 16);
  await renderPng(logoMarkSvg, resolve(brandDir, "icon-32.png"), 32);
  await renderPng(logoMarkSvg, resolve(brandDir, "apple-touch-icon-180.png"), 180);
  await renderPng(logoMarkSvg, resolve(brandDir, "icon-192.png"), 192);
  await renderPng(logoMarkSvg, resolve(brandDir, "icon-512.png"), 512);

  const ogSvg = Buffer.from(buildOgSvg());
  await sharp(ogSvg).png({ compressionLevel: 9 }).toFile(resolve(publicDir, "og-image.png"));

  await writeFile(
    resolve(brandDir, "README.txt"),
    [
      "HeadlessOS brand assets generated by scripts/generate-brand-assets.mjs",
      "",
      "Files:",
      "- icon-16.png",
      "- icon-32.png",
      "- apple-touch-icon-180.png",
      "- icon-192.png",
      "- icon-512.png",
      "",
      "The source mark is src/assets/logo-mark.svg",
    ].join("\n"),
  );

  console.log("Generated brand PNG assets and og-image.png");
};

run().catch((error) => {
  console.error(error);
  process.exit(1);
});
